int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	while (i <= 2147483647)
	{
		if (i * i == nb)
			return (i);
		if (i * i > nb)
			return (0);
		i++;
	}
	if (nb <= 0)
		return (0);
	return (0);
}
