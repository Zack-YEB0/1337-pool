int	ft_iterative_factorial(int nb)
{
	int	i;
	int	r;

	i = 1;
	r = 1;
	if (nb >= 0 && nb <= 12)
	{
		while (i <= nb)
		{
			r *= i;
			i++;
		}
		return (r);
	}
	return (0);
}
